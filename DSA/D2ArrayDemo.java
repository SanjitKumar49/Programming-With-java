public class D2ArrayDemo {
    public static void main(String[] args) {
        int array[][]={{1,2,3,4},{6,7},{7,8,9,0,1}
    };
    for (int i = 0; i < array.length; i++) {
        for (int j = 0; j < array[i].length; j++) {
            System.out.print(array[i][j] +" ");
            
        }
        System.out.println("");
    }
    }
    
}
